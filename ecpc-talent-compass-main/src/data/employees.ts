export type JobGrade = 'G1' | 'G2' | 'G3' | 'G4';
export type Direction = 'Operations' | 'Finance' | 'HR' | 'Sales & Marketing' | 'IT';
export type JobCategory = 'Technical' | 'Management' | 'Administrative' | 'Sales';
export type RiskLevel = 'Low' | 'Medium' | 'High';
export type ReadinessTimeline = 'Ready Now' | '1-3 Years' | 'More than 3 Years';

export interface Employee {
  id: string;
  name: string;
  jobTitle: string;
  jobGrade: JobGrade;
  direction: Direction;
  department: string;
  jobCategory: JobCategory;
  performance: number; // 1-3
  evolutionPotential: number; // 1-3
  riskOfLoss: RiskLevel;
  impactOfLoss: RiskLevel;
  nextRole: string;
  readiness: ReadinessTimeline;
  last3YearsPerformance: number; // 1-5
  assessmentHistory: Assessment[];
}

export interface Assessment {
  id: string;
  employeeId: string;
  managerId: string;
  year: number;
  performanceContribution: number;
  evolutionPotential: number;
  riskOfLoss: RiskLevel;
  impactOfLoss: RiskLevel;
  nextRole: string;
  readiness: ReadinessTimeline;
  comments: string;
  createdDate: string;
  updatedDate: string;
}

